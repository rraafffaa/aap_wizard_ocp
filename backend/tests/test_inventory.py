import copy

import pytest

from app.models import DeploymentConfig
from app.inventory import InventoryGenerator


def _render(cfg_dict: dict) -> str:
    return InventoryGenerator(DeploymentConfig(**cfg_dict)).render()


def _validate(cfg_dict: dict) -> list[str]:
    return InventoryGenerator(DeploymentConfig(**cfg_dict)).validate()


