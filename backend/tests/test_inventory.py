class TestInventoryValidation:
    """Tests for inventory configuration validation."""

    def test_valid_growth_config_no_errors(self, default_config):
        errors = _validate(default_config)
        assert errors == []

    def test_valid_enterprise_config_no_errors(self, enterprise_config):
        errors = _validate(enterprise_config)
        assert errors == []

    def test_empty_gateway_host_error(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["gateway"]["hosts"] = []
        errors = _validate(cfg)
        assert any("gateway host" in e.lower() for e in errors)

    def test_empty_controller_host_error(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["controller"]["hosts"] = []
        errors = _validate(cfg)
        assert any("controller host" in e.lower() for e in errors)

    def test_missing_admin_password_error(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["gateway"]["admin_password"] = ""
        errors = _validate(cfg)
        assert any("gateway admin password" in e.lower() for e in errors)

    def test_missing_db_admin_password_error(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["database"]["admin_password"] = ""
        errors = _validate(cfg)
        assert any("database admin password" in e.lower() for e in errors)

    def test_enterprise_needs_multiple_gateways(self, enterprise_config):
        cfg = copy.deepcopy(enterprise_config)
        cfg["gateway"]["hosts"] = ["gw1.example.org"]
        errors = _validate(cfg)
        assert any("2 gateway" in e.lower() for e in errors)

    def test_enterprise_needs_multiple_controllers(self, enterprise_config):
        cfg = copy.deepcopy(enterprise_config)
        cfg["controller"]["hosts"] = ["ctrl1.example.org"]
        errors = _validate(cfg)
        assert any("2 controller" in e.lower() for e in errors)

    def test_online_needs_registry_credentials(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["registry"]["username"] = ""
        errors = _validate(cfg)
        assert any("registry username" in e.lower() for e in errors)

    def test_online_needs_registry_password(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["registry"]["password"] = ""
        errors = _validate(cfg)
        assert any("registry password" in e.lower() for e in errors)

    def test_disconnected_skips_registry_check(self, disconnected_config):
        cfg = copy.deepcopy(disconnected_config)
        cfg["registry"]["username"] = ""
        cfg["registry"]["password"] = ""
        errors = _validate(cfg)
        assert not any("registry" in e.lower() for e in errors)

    def test_multiple_errors_returned(self, default_config):
        cfg = copy.deepcopy(default_config)
        cfg["gateway"]["admin_password"] = ""
        cfg["database"]["admin_password"] = ""
        cfg["registry"]["username"] = ""
        errors = _validate(cfg)
        assert len(errors) >= 3
