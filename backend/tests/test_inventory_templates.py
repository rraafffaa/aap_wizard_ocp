    # Diff
    def test_diff_identical(self, engine, default_config):
        inv = engine.from_config(default_config)
        diffs = engine.diff_inventories(inv, inv)
        assert diffs == []

    def test_diff_host_added(self, engine):
        base = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                )
            }
        )
        new = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[
                        InventoryHost(hostname="web1.example.org"),
                        InventoryHost(hostname="web2.example.org"),
                    ],
                )
            }
        )
        diffs = engine.diff_inventories(base, new)
        host_added = [d for d in diffs if d["type"] == "host_added"]
        assert len(host_added) == 1
        assert host_added[0]["host"] == "web2.example.org"
        assert host_added[0]["group"] == "web"

    def test_diff_variable_changed(self, engine):
        base = Inventory(
            groups={"web": InventoryGroup(name="web", hosts=[InventoryHost("web1")])},
            all_variables={"foo": "bar"},
        )
        new = Inventory(
            groups={"web": InventoryGroup(name="web", hosts=[InventoryHost("web1")])},
            all_variables={"foo": "baz"},
        )
        diffs = engine.diff_inventories(base, new)
        var_changed = [d for d in diffs if d["type"] == "all_var_changed"]
        assert len(var_changed) == 1
        assert var_changed[0]["key"] == "foo"
        assert var_changed[0]["old"] == "bar"
        assert var_changed[0]["new"] == "baz"

    # Merge
    def test_merge_adds_groups(self, engine):
        base = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                )
            }
        )
        overlay = Inventory(
            groups={
                "db": InventoryGroup(
                    name="db",
                    hosts=[InventoryHost(hostname="db1.example.org")],
                )
            }
        )
        merged = engine.merge_inventories(base, overlay)
        assert "web" in merged.groups
        assert "db" in merged.groups
        assert merged.groups["db"].hosts[0].hostname == "db1.example.org"

    def test_merge_combines_hosts(self, engine):
        base = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                )
            }
        )
        overlay = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web2.example.org")],
                )
            }
        )
        merged = engine.merge_inventories(base, overlay)
        hostnames = [h.hostname for h in merged.groups["web"].hosts]
        assert "web1.example.org" in hostnames
        assert "web2.example.org" in hostnames

    def test_merge_overlay_vars_win(self, engine):
        base = Inventory(
            groups={"web": InventoryGroup(name="web", hosts=[InventoryHost("web1")])},
            all_variables={"foo": "base_value"},
        )
        overlay = Inventory(
            groups={},
            all_variables={"foo": "overlay_value"},
        )
        merged = engine.merge_inventories(base, overlay)
        assert merged.all_variables["foo"] == "overlay_value"

