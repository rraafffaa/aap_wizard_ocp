"""Comprehensive tests for the inventory template engine."""
import json
import pytest
import yaml

from app.services.inventory_templates import (
    InventoryTemplateEngine,
    Inventory,
    InventoryGroup,
    InventoryHost,
)


class TestInventoryTemplateEngine:
    @pytest.fixture
    def engine(self):
        return InventoryTemplateEngine()

    # Template registration
    def test_builtin_templates_registered(self, engine):
        templates = engine.available_templates
        assert len(templates) >= 5
        names = [t["name"] for t in templates]
        assert "growth" in names
        assert "enterprise_basic" in names
        assert "enterprise_ha" in names
        assert "air_gapped" in names
        assert "development" in names

    def test_growth_template_exists(self, engine):
        inv = engine.from_template("growth", {})
        assert "automationgateway" in inv.groups
        assert "automationcontroller" in inv.groups
        assert "automationhub" in inv.groups
        assert "automationeda" in inv.groups
        assert "database" in inv.groups

    def test_enterprise_basic_template_exists(self, engine):
        inv = engine.from_template("enterprise_basic", {})
        assert "automationgateway" in inv.groups
        assert len(inv.groups["automationgateway"].hosts) == 2
        assert "redis" in inv.groups

    def test_enterprise_ha_template_exists(self, engine):
        inv = engine.from_template("enterprise_ha", {})
        assert "execution_nodes" in inv.groups
        assert len(inv.groups["automationhub"].hosts) == 2
        assert "redis_mode" in inv.all_variables

    # INI rendering
    def test_render_ini_empty_inventory(self, engine):
        inv = Inventory()
        result = engine.render_ini(inv)
        assert "# AAP Inventory" in result
        assert "generated by InventoryTemplateEngine" in result

    def test_render_ini_single_group(self, engine):
        inv = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                )
            }
        )
        result = engine.render_ini(inv)
        assert "[web]" in result
        assert "web1.example.org" in result

    def test_render_ini_multiple_groups(self, engine):
        inv = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                ),
                "db": InventoryGroup(
                    name="db",
                    hosts=[InventoryHost(hostname="db1.example.org")],
                ),
            }
        )
        result = engine.render_ini(inv)
        assert "[web]" in result
        assert "[db]" in result
        assert "web1.example.org" in result
        assert "db1.example.org" in result

    def test_render_ini_group_vars(self, engine):
        inv = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                    variables={"ansible_user": "deploy", "port": 8080},
                )
            }
        )
        result = engine.render_ini(inv)
        assert "[web:vars]" in result
        assert "ansible_user=deploy" in result
        assert "port=8080" in result

    def test_render_ini_host_vars(self, engine):
        inv = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[
                        InventoryHost(
                            hostname="web1.example.org",
                            variables={"ansible_host": "192.168.1.10"},
                        )
                    ],
                )
            }
        )
        result = engine.render_ini(inv)
        assert "web1.example.org" in result
        assert "ansible_host=192.168.1.10" in result

    def test_render_ini_children(self, engine):
        inv = Inventory(
            groups={
                "prod": InventoryGroup(
                    name="prod",
                    hosts=[],
                    children=["web", "db"],
                ),
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                ),
                "db": InventoryGroup(
                    name="db",
                    hosts=[InventoryHost(hostname="db1.example.org")],
                ),
            }
        )
        result = engine.render_ini(inv)
        assert "[prod:children]" in result
        assert "web" in result
        assert "db" in result

    def test_render_ini_all_vars(self, engine):
        inv = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                )
            },
            all_variables={"ansible_python_interpreter": "/usr/bin/python3"},
        )
        result = engine.render_ini(inv)
        assert "[all:vars]" in result
        assert "ansible_python_interpreter=/usr/bin/python3" in result

    def test_render_ini_special_chars_escaped(self, engine):
        inv = Inventory(
            groups={
                "web": InventoryGroup(
                    name="web",
                    hosts=[InventoryHost(hostname="web1.example.org")],
                    variables={"path": "/opt/app with spaces", "comment": "value#hash"},
                )
            }
        )
        result = engine.render_ini(inv)
        assert "[web:vars]" in result
        # Values with spaces or special chars should be quoted
        assert "path=" in result
        assert "'" in result or "spaces" in result

